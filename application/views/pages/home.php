<p>My name is Ryan Bowers - Multi-Media Designer. I was born in Edmonton, and raised in Belfast, Ireland. I returned to Edmonton in 2007. Since then I have gained just over 10 years experience in print and online media. I am a graduate of the University of Ulster, Design and Communication.</p>

<p>I like sublime text for coding, although I used dreamweaver when starting out learning to code. Photoshop for image editing and Illustator for building my vector art. I would always have Chrome Dev tools open and the console log I find very helpful for debugging.</p>

<p>I have been learning PHP as I find it has good documentation. I have completed my first codeigniter MVC framework app, and soon to pick up a javascript framework like Angular as I like the MVC libraries that allows me to connect my models data to my view pages.</p>

<p>On occasions when I had a challenging interactions, not so much with clients but with sale reps. For example if the online advertisment is becoming to busy and text heavily, I do try to explain that this is just way too much information to be placed in this advertisment for an ad that is going to be viewd by a person for maybe 6 to 7 seconds and then have the ad overlooked. If the rep is to instist on running the advertisment anyway, I would also build another creative to show to the client side by side, just incase there is a better solution.</p>

<p>I get passionate about design - when I get as close to perfection as possible to visualize a clean solution taken from a clients brief. And in doing so, under a given deadline. Whenever a client instantly gets this solutions without needing much explanation of the final design.</p>


